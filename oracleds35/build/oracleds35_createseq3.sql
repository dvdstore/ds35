DECLARE 
  REVIEW_ROWS3 NUMBER;
  HELP_ROWS3 NUMBER;

BEGIN

  SELECT count(*) INTO REVIEW_ROWS3  from "DS3"."REVIEWS3";

REVIEW_ROWS3 := REVIEW_ROWS3 + 1;

EXECUTE IMMEDIATE '
CREATE SEQUENCE "DS3"."REVIEWID_SEQ3"
  INCREMENT BY 1
  START WITH ' || REVIEW_ROWS3 || '
  MAXVALUE 1.0E28
  MINVALUE 1
  NOCYCLE
  CACHE 100000
  NOORDER'
  ;

  SELECT count(*) INTO HELP_ROWS3  from "DS3"."REVIEWS_HELPFULNESS3";

HELP_ROWS3 := HELP_ROWS3 + 1;

EXECUTE IMMEDIATE '
CREATE SEQUENCE "DS3"."REVIEWHELPFULNESSID_SEQ3"
  INCREMENT BY 1
  START WITH ' || HELP_ROWS3 || '
  MAXVALUE 1.0E28
  MINVALUE 1
  NOCYCLE
  CACHE 100000
  NOORDER'
  ;
END;
/
EXIT;

