DECLARE 
  REVIEW_ROWS5 NUMBER;
  HELP_ROWS5 NUMBER;

BEGIN

  SELECT count(*) INTO REVIEW_ROWS5  from "DS3"."REVIEWS5";

REVIEW_ROWS5 := REVIEW_ROWS5 + 1;

EXECUTE IMMEDIATE '
CREATE SEQUENCE "DS3"."REVIEWID_SEQ5"
  INCREMENT BY 1
  START WITH ' || REVIEW_ROWS5 || '
  MAXVALUE 1.0E28
  MINVALUE 1
  NOCYCLE
  CACHE 100000
  NOORDER'
  ;

  SELECT count(*) INTO HELP_ROWS5  from "DS3"."REVIEWS_HELPFULNESS5";

HELP_ROWS5 := HELP_ROWS5 + 1;

EXECUTE IMMEDIATE '
CREATE SEQUENCE "DS3"."REVIEWHELPFULNESSID_SEQ5"
  INCREMENT BY 1
  START WITH ' || HELP_ROWS5 || '
  MAXVALUE 1.0E28
  MINVALUE 1
  NOCYCLE
  CACHE 100000
  NOORDER'
  ;
END;
/
EXIT;

