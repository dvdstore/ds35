DECLARE 
  REVIEW_ROWS1 NUMBER;
  HELP_ROWS1 NUMBER;

BEGIN

  SELECT count(*) INTO REVIEW_ROWS1  from "DS3"."REVIEWS1";

REVIEW_ROWS1 := REVIEW_ROWS1 + 1;

EXECUTE IMMEDIATE '
CREATE SEQUENCE "DS3"."REVIEWID_SEQ1"
  INCREMENT BY 1
  START WITH ' || REVIEW_ROWS1 || '
  MAXVALUE 1.0E28
  MINVALUE 1
  NOCYCLE
  CACHE 100000
  NOORDER'
  ;

  SELECT count(*) INTO HELP_ROWS1  from "DS3"."REVIEWS_HELPFULNESS1";

HELP_ROWS1 := HELP_ROWS1 + 1;

EXECUTE IMMEDIATE '
CREATE SEQUENCE "DS3"."REVIEWHELPFULNESSID_SEQ1"
  INCREMENT BY 1
  START WITH ' || HELP_ROWS1 || '
  MAXVALUE 1.0E28
  MINVALUE 1
  NOCYCLE
  CACHE 100000
  NOORDER'
  ;
END;
/
EXIT;

