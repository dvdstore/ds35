DECLARE 
  REVIEW_ROWS2 NUMBER;
  HELP_ROWS2 NUMBER;

BEGIN

  SELECT count(*) INTO REVIEW_ROWS2  from "DS3"."REVIEWS2";

REVIEW_ROWS2 := REVIEW_ROWS2 + 1;

EXECUTE IMMEDIATE '
CREATE SEQUENCE "DS3"."REVIEWID_SEQ2"
  INCREMENT BY 1
  START WITH ' || REVIEW_ROWS2 || '
  MAXVALUE 1.0E28
  MINVALUE 1
  NOCYCLE
  CACHE 100000
  NOORDER'
  ;

  SELECT count(*) INTO HELP_ROWS2  from "DS3"."REVIEWS_HELPFULNESS2";

HELP_ROWS2 := HELP_ROWS2 + 1;

EXECUTE IMMEDIATE '
CREATE SEQUENCE "DS3"."REVIEWHELPFULNESSID_SEQ2"
  INCREMENT BY 1
  START WITH ' || HELP_ROWS2 || '
  MAXVALUE 1.0E28
  MINVALUE 1
  NOCYCLE
  CACHE 100000
  NOORDER'
  ;
END;
/
EXIT;

