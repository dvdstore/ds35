DECLARE 
  REVIEW_ROWS4 NUMBER;
  HELP_ROWS4 NUMBER;

BEGIN

  SELECT count(*) INTO REVIEW_ROWS4  from "DS3"."REVIEWS4";

REVIEW_ROWS4 := REVIEW_ROWS4 + 1;

EXECUTE IMMEDIATE '
CREATE SEQUENCE "DS3"."REVIEWID_SEQ4"
  INCREMENT BY 1
  START WITH ' || REVIEW_ROWS4 || '
  MAXVALUE 1.0E28
  MINVALUE 1
  NOCYCLE
  CACHE 100000
  NOORDER'
  ;

  SELECT count(*) INTO HELP_ROWS4  from "DS3"."REVIEWS_HELPFULNESS4";

HELP_ROWS4 := HELP_ROWS4 + 1;

EXECUTE IMMEDIATE '
CREATE SEQUENCE "DS3"."REVIEWHELPFULNESSID_SEQ4"
  INCREMENT BY 1
  START WITH ' || HELP_ROWS4 || '
  MAXVALUE 1.0E28
  MINVALUE 1
  NOCYCLE
  CACHE 100000
  NOORDER'
  ;
END;
/
EXIT;

